// Package mixingen provides code generation for Mixin patterns
// 将 Mixin 方法直接生成到目标类型中
package mixingen

import (
	"bytes"
	"go/format"
	"io"
	"os"
	"reflect"
	"runtime"
	"strings"
	"text/template"
)

// Generator Mixin 代码生成器
type Generator struct {
	Package   string
	Target    string
	Mixin     string
	Methods   []MethodDef
	Imports   []string
	ExtraCode string
}

// MethodDef 方法定义
type MethodDef struct {
	Name        string
	Params      string
	Results     string
	Body        string
	Comment     string
	IsGenerated bool
}

// Config 生成器配置
type Config struct {
	Package   string
	Target    string
	Mixin     string
	Methods   []MethodDef
	Imports   []string
	ExtraCode string
}

// NewGenerator 创建生成器
func NewGenerator(config Config) *Generator {
	return &Generator{
		Package:   config.Package,
		Target:    config.Target,
		Mixin:     config.Mixin,
		Methods:   config.Methods,
		Imports:   config.Imports,
		ExtraCode: config.ExtraCode,
	}
}

// Generate 生成代码
func (g *Generator) Generate() ([]byte, error) {
	var buf bytes.Buffer

	if err := headerTemplate.Execute(&buf, g); err != nil {
		return nil, err
	}

	if err := importsTemplate.Execute(&buf, g); err != nil {
		return nil, err
	}

	if err := methodsTemplate.Execute(&buf, g); err != nil {
		return nil, err
	}

	if g.ExtraCode != "" {
		buf.WriteString("\n")
		buf.WriteString(g.ExtraCode)
	}

	return format.Source(buf.Bytes())
}

// WriteTo 写入文件
func (g *Generator) WriteTo(w io.Writer) (int64, error) {
	code, err := g.Generate()
	if err != nil {
		return 0, err
	}
	n, err := w.Write(code)
	return int64(n), err
}

// WriteFile 写入文件
func (g *Generator) WriteFile(filename string) error {
	code, err := g.Generate()
	if err != nil {
		return err
	}
	return os.WriteFile(filename, code, 0644)
}

var headerTemplate = template.Must(template.New("header").Parse(`// Code generated by mixingen. DO NOT EDIT.
// Run "go generate" to update this file.
// 
// This file contains Mixin methods generated from {{.Mixin}} into {{.Target}}

package {{.Package}}
`))

var importsTemplate = template.Must(template.New("imports").Parse(`
import (
{{- range .Imports}}
	{{.}}
{{- end}}
)
`))

var methodsTemplate = template.Must(template.New("methods").Parse(`
{{- range .Methods}}
{{if .Comment}}
// {{.Comment}}
{{- end}}
func (a *{{$.Target}}) {{.Name}}({{.Params}}) {{.Results}} {
{{.Body}}
}
{{- end}}
`))


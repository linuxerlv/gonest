// ParseMethodFromFunc 从函数解析方法定义
func ParseMethodFromFunc(fn interface{}) MethodDef {
	t := reflect.TypeOf(fn)
	if t.Kind() != reflect.Func {
		return MethodDef{}
	}

	name := strings.TrimPrefix(runtime.FuncForPC(reflect.ValueOf(fn).Pointer()).Name(), "github.com/linuxerlv/gonest/core.")
	name = strings.Split(name, ".")[0]

	var params, results strings.Builder

	for i := 0; i < t.NumIn(); i++ {
		if i > 0 {
			params.WriteString(", ")
		}
		params.WriteString(t.In(i).String())
	}

	for i := 0; i < t.NumOut(); i++ {
		if i > 0 {
			results.WriteString(", ")
		}
		results.WriteString(t.Out(i).String())
	}

	return MethodDef{
		Name:    name,
		Params:  params.String(),
		Results: results.String(),
	}
}
